package Lecture1;

public class AssignmentOperators {
    public static void main(String[] args) {
        int x = 9; // when we use = it means the 9 is successfully stored in the x
        System.out.println(x);
        //after using the assignment operator the value of the same variable will be change
        x -= 5;
        System.out.println(x);
    }
}
